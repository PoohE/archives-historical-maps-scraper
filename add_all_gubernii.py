# ── Загрузить уже существующие записи чтобы не дублировать ──────────────────
print("Загружаем существующие записи...")
existing: set[str] = set()
cursor = None
while True:
    body = {"page_size": 100}
    if cursor:
        body["start_cursor"] = cursor
    result = api("POST",
                 f"https://api.notion.com/v1/databases/{DB_ADMIN_ID}/query",
                 body)
    for page in result.get("results", []):
        t = page["properties"].get("Название", {}).get("title", [])
        if t:
            existing.add(t[0]["plain_text"])
    if not result.get("has_more"):
        break
    cursor = result.get("next_cursor")
print(f"  Уже в базе: {len(existing)} записей")

