"""
Единый реестр триггер-слов для поиска историко-картографических источников.

Источники:
  1. «фильтр запросов центральные архивы.txt» — исходный список и анализ
  2. Статистический анализ kaluga.csv + perm.csv (106 названий)
     скрипт analyze_titles.py → output/trigger_analysis.txt
"""

# ─── Базовые триггеры (тип документа) ──────────────────────────────────────
# Всегда означают картографический источник
PRIMARY: tuple[str, ...] = (
    "карт",        # карта, карты, картографическ…
    "план",        # план, планы, плановый…
    "схем",        # схема, схемы…
    "атлас",       # атлас, атласу, атласов…
    "съёмк",       # съёмка, съёмки, съёмочный…
    "съемк",       # дореформенное написание
    "чертёж",      # чертёж, чертежи…
    "чертеж",      # дореформенное написание
)

# ─── Видовые маркеры (серийные типы карт) ──────────────────────────────────
SERIES: tuple[str, ...] = (
    "топографическ",        # топографическая, топографическое описание
    "военно-топограф",      # военно-топографическая карта
    "семитопограф",         # семитопографическая карта (серия МВД)
    "геометрическ",         # геометрическая карта (Генеральное межевание)
    "глазомерн",            # глазомерная съёмка (рекогносцировки)
    "рекогносц",            # рекогносцированная, рекогносцировка
    "генеральн",            # генеральный план, Генеральное межевание
    "межеван",              # межевание, межевой план, межевая книга
    "лесн",                 # лесные дачи — всегда имеют межевые планы
    "дач",                  # дача (межевая единица), лесная дача
)

# ─── Маркеры масштаба (почти всегда = картографический источник) ────────────
SCALE: tuple[str, ...] = (
    "дюйме",       # «в 1 дюйме N верст» — стандартная формула масштаба
    "верст",       # «верста», «версты», «вёрсты»
    "вёрст",
    "масштаб",     # прямое упоминание масштаба
)

# ─── Маркеры формы (рукопись / издание) ─────────────────────────────────────
FORM: tuple[str, ...] = (
    "рукописн",    # рукописная карта — важно для неизданных
    "рук.",        # сокращение «рук.» в каталожных описаниях
    "граверн",     # гравированная карта (издание)
    "литограф",    # литографированная карта
    "печатн",      # печатный экземпляр
)

# ─── Исторические топонимы (XVIII в. — до 1796 г.) ──────────────────────────
HISTORICAL: tuple[str, ...] = (
    "наместничеств",  # Калужское/Пермское/Смоленское/Ярославское наместничество
                       # до губернской реформы 1796 г.
)

# ─── Специфика Пермской губернии ────────────────────────────────────────────
PERM_SPECIFIC: tuple[str, ...] = (
    "завод",       # заводские планы (Невьянский, Чердынский и др.)
    "горн",        # горные заводы
    "рудник",      # рудники — часто имеют топографические приложения
)

# ─── Все позитивные триггеры (объединение) ──────────────────────────────────
ALL_POSITIVE: tuple[str, ...] = (
    PRIMARY + SERIES + SCALE + FORM + HISTORICAL + PERM_SPECIFIC
)

# ─── Сомнительные (агент помечает поле «Сомнительных» = да) ────────────────
# Встречаются рядом с картами, но сами по себе ≠ картографический источник
DOUBTFUL: tuple[str, ...] = (
    "статистическ",    # статистическое описание — текстовый источник
    "перепис",         # перепись населения
    "список сел",      # списки селений
    "список насел",    # списки населённых мест
    "наделов",         # земельные наделы (таблицы) без карты
    "волост",          # волостной сборник без картографии
    "справочн",        # справочная книга
    "адрес-календар",  # адрес-календарь
)


def is_cartographic(title: str) -> bool:
    """True если название содержит хотя бы один позитивный триггер."""
    t = title.lower()
    return any(kw in t for kw in ALL_POSITIVE)


def is_doubtful(title: str) -> bool:
    """
    True если название содержит сомнительный маркер.
    Используется для поля «Сомнительных» (агент ставит «да»).
    Вызывать только если is_cartographic() вернул False.
    """
    t = title.lower()
    return any(kw in t for kw in DOUBTFUL)


def classify(title: str) -> str:
    """
    Классифицирует название:
      'positive'  — однозначно картографический источник
      'doubtful'  — сомнительно, требует проверки
      'negative'  — не картографический, пропустить
    """
    if is_cartographic(title):
        return "positive"
    if is_doubtful(title):
        return "doubtful"
    return "negative"
