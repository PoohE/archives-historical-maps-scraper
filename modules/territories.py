# ── Уезды по губерниям (для генерации дополнительных запросов) ───────────────

UYEZD_QUERIES: dict[str, tuple[str, ...]] = {
    "калужская":   KALUGA,
    "пермская":    PERM,
    "смоленская":  SMOLENSK,
    "ярославская": YAROSLAVL,
}

ALL_UYEZDS: tuple[str, ...] = KALUGA + PERM + SMOLENSK + YAROSLAVL


def get_queries(territory: str) -> list[str]:
    """
    Возвращает список поисковых запросов для заданной территории.
    territory — губерния или уезд (частичное совпадение).

    Примеры:
        get_queries("Калужская") → ["Калужская губерния", "Калужское наместничество",
                                    "Боровский уезд", ..., "Тарусский уезд"]
        get_queries("Козельский уезд") → ["Козельский уезд"]
    """
    t = territory.lower()
    queries: list[str] = []

    # Совпадение с губернией
    for key, gub_list in GUBERNIA_QUERIES.items():
        if key in t or t in key:
            queries.extend(gub_list)
            queries.extend(UYEZD_QUERIES[key])
            return queries

    # Совпадение с уездом
    for uyezd in ALL_UYEZDS:
        if t in uyezd.lower() or uyezd.lower() in t:
            queries.append(uyezd)

    return queries or [territory]


def expand_query(base_query: str, territory: str) -> list[str]:
    """
    Разворачивает один поисковый запрос в список с учётом всех территорий.

    expand_query("план", "Калужская") →
        ["план Калужская губерния", "план Калужское наместничество",
         "план Боровский уезд", ..., "план Тарусский уезд"]
    """
    return [f"{base_query} {q}" for q in get_queries(territory)]
