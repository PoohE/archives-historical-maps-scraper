"""
Полный список территорий (губернии + уезды) для поиска архивов.
Конец XIX — начало XX века.
Формат: словари по ключам губерний для совместимости с run_search.py
"""

# Губернии и наместничества (основные 4 административной единицы)
GUBERNIA_QUERIES = {
    "калужская": ["Калужская губерния", "Калужское наместничество"],
    "пермская": ["Пермская губерния", "Пермское наместничество"],
    "смоленская": ["Смоленская губерния", "Смоленское наместничество"],
    "ярославская": ["Ярославская губерния", "Ярославское наместничество"],
}

# Уезды (41 единица)
UYEZD_QUERIES = {
    "калужская": [
        "Калужский уезд", "Мосальск", "Медынь", "Мещовск", "Перемышль",
        "Козельск", "Лихвин", "Тарусский", "Малоярославецкий",
        "Думиничский", "Карачев", "Болхов",
    ],
    "пермская": [
        "Пермский уезд", "Оса", "Соликамск", "Чердынь", "Верхотурье",
        "Орёл", "Кунгур", "Кизел", "Ирбит",
    ],
    "смоленская": [
        "Смоленский уезд", "Ржев", "Вязьма", "Дорогобуж", "Сафоново",
        "Сычёвка", "Духовщина", "Гжатск", "Красный", "Рославль", "Велиж",
    ],
    "ярославская": [
        "Ярославский уезд", "Углич", "Мышкин", "Ростов", "Переславль-Залесский",
        "Ярославец", "Мологский", "Пошехонье", "Мышкинский",
    ],
}

# Полный список всех 49 территорий (для совместимости)
def _build_all_territories():
    all_terr = []
    for gub_key in ("калужская", "пермская", "смоленская", "ярославская"):
        all_terr.extend(GUBERNIA_QUERIES[gub_key])
        all_terr.extend(UYEZD_QUERIES[gub_key])
    return all_terr

TERRITORIES = _build_all_territories()

# Проверка: 4×2 (губ+наместн) + 41 уезд = 49
assert sum(len(v) for v in GUBERNIA_QUERIES.values()) == 8  # 4 губ + 4 наместн
assert sum(len(v) for v in UYEZD_QUERIES.values()) == 41    # 41 уезд
assert len(TERRITORIES) == 49
