def make_page(entry: dict) -> dict:
    props = {
        'Название': {'title': [{'text': {'content': entry['название']}}]},
        'Тип':      {'select': {'name': entry['тип']}},
    }
    if entry.get('сайт'):
        props['Сайт'] = {'url': entry['сайт']}
    if entry.get('аббрев'):
        props['Аббревиатура'] = {'rich_text': [{'text': {'content': entry['аббрев']}}]}
    if entry.get('город'):
        props['Город'] = {'rich_text': [{'text': {'content': entry['город']}}]}
    if entry.get('фонды'):
        props['Ключевые фонды'] = {'rich_text': [{'text': {'content': entry['фонды']}}]}
    if entry.get('особенн'):
        props['Особенности'] = {'rich_text': [{'text': {'content': entry['особенн']}}]}
    if entry.get('приорит'):
        props['Приоритет'] = {'select': {'name': entry['приорит']}}
    return {'parent': {'database_id': DB_ID}, 'properties': props}


def add_page(entry: dict):
    body = json.dumps(make_page(entry)).encode('utf-8')
    req = urllib.request.Request(
        'https://api.notion.com/v1/pages',
        data=body, headers=HEADERS, method='POST')
    try:
        with urllib.request.urlopen(req) as r:
            result = json.load(r)
        print(f"  ✓  {entry['название']}")
        return True
    except urllib.error.HTTPError as e:
        err = e.read().decode()
        print(f"  ✗  {entry['название']}: {e.code} {err[:120]}")
        return False


